<h2 align="left" style="margin-left:45px;font-family: 'Playfair Display', serif;font-size:60px">Rahul Gandhi</h2>

<div style="font-family:'Courier New', Courier, monospace; margin-left:50px;margin-right:60px;"><strong><p style="font-size: 20px;">A leader is one who knows the way, goes the way and shows the way”. Over last few years, the way he’s turned up and taken politics in stride, he has just infused more prominent expectation and self-confidence in every single one of us. It’s not restricted to his commitment to the Congress, but rather his bigger duty towards national objectives. <br>
</p></strong>

<strong><h3>Let’s take a look at Rahul Gandhi education and achievements in last few years</h3>
<hr align ="centre" style="width:80%;" noshade="5.5">

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">Rahul Gandhi goes into politics</h3>
<p style="font-size: 20px;margin-left:20px">

After acquiring degrees in International Relations and Philosophy from the Universities of Rollins and Cambridge and working in London, it was not expected that Rahul could ever join politics. Surprising everyone, this previously considered shy person chose an open stage to voice his opinion. Rahul went into politics in May 2004 when he chose to challenge the Lok Sabha elections from the family constituency Amethi, which his father Rajiv Gandhi had once held.
<br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">Selected as the General Secretary, IYC and NSUI</h3>
<p style="font-size: 20px;margin-left:20px">

In September 2007, Rahul Gandhi achievement list got its first mark when he was selected the ‘General Secretary’ accountable for the National Students Union of India (NSUI) and Indian Youth Congress (IYC). During this time, he promised to change youth politics. Therefore, keeping up to his words, in November 2008, he held meetings at his 12, Tughlak Lane apartment in New Delhi to at least 40 individuals who will be instrumental in the formation of ‘thinktank’ of the Indian Youth Congress (IYC). This bold action creates a positive change to the association that was significantly needed at that moment. Not only that, it was under his leadership that IYC and NSUI saw a sensational increment in the number of members from two lakhs to twenty five lakhs. Be it his individual endeavours or his breakthrough vision, at last, we have a leader who has faith in thinking first and acting later.
<br><br>


<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">The Youth Icon</h3>
<p style="font-size: 20px;margin-left:20px">

Have you ever given it a thought that why Rahul Gandhi has become a beloved leader by the youth of the country? His action toward NSUI and IYC make this fifth-generation scion of the politically powerful Nehru– Gandhi family cherished youth icon in this nation. His efforts to acquire transparency to the political framework by introducing the youngsters in strategic posts of government have won him wide praise. He strongly believes that the youth can change the way the Indian administration works and bring the country to new heights. That is the reason he continuously works toward providing equal opportunity in the election to the young brigade.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">UP Elections</h3>
<p style="font-size: 20px;margin-left:20px">

The results of 2009 General Elections demonstrated that the inheritance of the Gandhi family and its profound association with the Congress Party isn’t only for namesake. Adding to Rahul Gandhi achievements, the biggest reason behind this victory was that there was no other political party other than Congress who had fielded such a large number of young candidates. What’s more, without any doubt, the credit for this triumph of UPA goes straight to Rahul Gandhi. He demonstrated his strength again by almost single-handedly winning in no less than 20 seats in Uttar Pradesh and overall 204 seats in the Lok Sabha. 
