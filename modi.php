<h2 align="left" style="margin-left:45px;font-family: 'Playfair Display', serif;font-size:60px">Narendra MODI</h2>

<div style="font-family:'Courier New', Courier, monospace; margin-left:50px;margin-right:60px;"><strong><p style="font-size: 20px;">Prime Minister Narendra Modi turned 70 today. He is the only non-Congress prime minister to have won two consecutive majorities in Lok Sabha elections.

In the six years as prime minister, Narendra Modi is credited to have introduced some long-awaited reforms in the country. However, with four years still remaining in his second term, his government faces a plethora of challenges as well.</p></strong>

<strong><h3>First, we list out five of PM Modi's achievements during the past six years.</h3>
<hr align ="centre" style="width:80%;" noshade="5.5">

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">GOODS AND SERVICES TAX</h3>
<p style="font-size: 20px;margin-left:20px">The Goods and Services Tax (GST) was in the pipeline for 17 years before it became a major tax reform in 2017. For long, India was seen as a non-friendly country for business owing to its complicated tax laws.

The GST subsumed 17 existing indirect taxes to make compliance of taxation laws by the business world simple. The rollout of GST through a special session of Parliament three years ago remains a major highlight of the Modi government.
<br><br>


<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">INSOLVENCY AND BANKRUPTCY CODE</h3>
<p style="font-size: 20px;margin-left:20px">The link between failed businesses and the consequent banking ailment was long seen as a problem area for economy growth and policy making. The Modi government enacted the Insolvency and Bankruptcy Code (IBC) law in 2016. The law was amended for the second time earlier this year to make it more attuned to ground realities.

It is described as the silver bullet to tackle India's chronic problem of non-performing assets. Banks have begun to recover debts that were earlier thought to be irretrievable.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">SWACHH BHARAT</h3>
<p style="font-size: 20px;margin-left:20px">Though it has its origin in the Nirmal Gram mission -- under Raghuvansh Prasad Singh-led rural development ministry -- of the Manmohan Singh government, the Swachh Bharat campaign is one of the big socio-political achievements of the Modi government.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">CLEANER, HEALTHIER KITCHEN</h3>
<p style="font-size: 20px;margin-left:20px">Another highlight of the government in the last six years and the one that played an equal political role -- besides the toilet scheme -- in getting Narendra Modi back to the PMO is free distribution of the LPG cylinders.
<br>

It was done through the Ujjwala Yojana, under which women in villages get free one LPG cylinder connection per household. The scheme has been so popular that at the end of the last fiscal, it overshot its target of 8 crore LPG connection.
<br>

Official data show that Ujjwala Yojana accounts for over 70 per cent growth in total domestic LPG connections in the country.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">POLITICAL ACCOMPLISHMENTS</h3>
<p style="font-size: 20px;margin-left:20px">The Modi government fulfilled some long-standing demands of the BJP, the party that leads the NDA government at the Centre. These include revoking the special status of Jammu and Kashmir under Article 370, construction of a Ram temple in Ayodhya -- PM Modi led the groundbreaking ceremony following a Supreme Court ruling, abolition of triple talaq -- a step seen largely forwarding the goal of bringing in a uniform civil code.
<br>

Yet, PM Modi faces a host of challenges for the remaining four years of his second tenure in the PMO.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">COVID-19</h3>
<p style="font-size: 20px;margin-left:20px">The Covid-19 pandemic has grabbed the world by its collar. India is currently the worst-affected country, not in terms of the total number of confirmed cases but as the biggest hotspot in the world.

The pandemic is spreading to more and more villages of the country where medical healthcare facilities are non-existent. The death toll has been rising with India recording maximum daily deaths in the world due to Covid-19.
<br>

Health experts have warned that the Covid-19 situation is unlikely to reverse any time soon. This remains the immediate biggest challenge for PM Modi and his government.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">SAGGING ECONOMY</h3>
<p style="font-size: 20px;margin-left:20px">PM Modi came to the power in 2014 on the promise of "achchhe din" (better days ahead). The early years of his tenure saw India clocking high growth in its Gross Domestic Product (GDP). But for the past two years or more, the economy has been facing a slowdown.
<br>
The economic slowdown turned into the first contraction in several decades due to the Covid-19 situation. The Modi government needs money to fund its health and social welfare programmes. That money can only come from revenue or taxes deposited by businesses.

</p>

