<h2 align="left" style="margin-left:45px;font-family: 'Playfair Display', serif;font-size:60px">Arvind Kejriwal</h2>

<div style="font-family:'Courier New', Courier, monospace; margin-left:50px;margin-right:60px;"><strong><p style="font-size: 20px;">Delhi Chief Minister and AAP national convener Arvind Kejriwal on Tuesday launched ‘AAP ka Report Card’, which describes the top 10 achievements of the government in the last five years, ahead of the Assembly elections due in February 2020. <br>

The report card will now be taken to 35 lakh households in the Capital as part of a door-to-door campaign from December 26 to January 7, according to the party.</p></strong>

<strong><h3>The report card has 10 major areas such as quality education for all, quality healthcare for all, affordable electricity for all, and piped water supply to every household among others.</h3>
<hr align ="centre" style="width:80%;" noshade="5.5">

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">Education</h3>
<p style="font-size: 20px;margin-left:20px">
Starting with work done in the field of education, Mr. Kejriwal said, “In the last 70 years, the quality of education and educational infrastructure had hugely declined. Government schools were reduced to nothing and private schools were operating like profit-making shops. It was a norm that your children will receive good education and study in an air-conditioned classroom if you have enough money, but if you do not have money, your children will not get quality education.”


<br><br>


<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">Unauthorised Colonies</h3>
<p style="font-size: 20px;margin-left:20px">
On regularising unauthorised colonies in the Capital, Mr. Kejriwal said: “It is easy to talk about unauthorised colonies, but it is difficult to work for them.”

“Talk can happen from Ramlila Maidan, but work doesn’t happen,” Mr. Kejriwal said in a veiled attack at Prime Minister Narendra Modi. “We have spent ₹8,000 crore in the last five years in unauthorised colonies,” he added.
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">Healthcare</h3>
<p style="font-size: 20px;">
Mr. Kejriwal said that the government has opened more than 400 mohalla clinics in Delhi and they have worked towards transforming the infrastructure of government hospitals. People can access free medicines, treatments, tests, and surgeries in all the Delhi government hospitals, he added.

“In 2015, there were 15,868 cases of dengue in Delhi. We travelled from one hospital to another to ensure that there were sufficient beds in all the hospitals and all the patients get proper care.”  
<br><br>

<h3  style="margin-left:20px;font-family: 'Playfair Display', serif;font-size:30px">Two years as a corruption crusader, five years as Chief Minister</h3>
<p style="font-size: 20px;margin-left:20px">
Two years as a corruption crusader, five years as Chief Minister
Kejriwal emerged from the shadows of social activist Anna Hazare in the anti-corruption protests in the early part of the last decade. They were demanding an independent Jan Lokpal, an ombudsman that would investigate corruption cases and conduct trials.
<br>

It was done through the Ujjwala Yojana, under which women in villages get free one LPG cylinder connection per household. The scheme has been so popular that at the end of the last fiscal, it overshot its target of 8 crore LPG connection.
<br>

Official data show that Ujjwala Yojana accounts for over 70 per cent growth in total domestic LPG connections in the country.
